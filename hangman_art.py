def draw_hangman(stage):
    hangman = {
        0: (
            "  ",
            "  ",
            "  ",
        ),

        1: {
            " o ",
            "   ",
            "   ",
        },

        2: {
            " o ",
            " | ",
            "   ",
        },

        3: {
            " o ",
            "/| ",
            "   ",
        },

        4: {
            " o ",
            "/|\\",
            "   ",
        },

        5: {
            " o ",
            "/|\\",
            "/  ",
        },

        6: {
            " o ",
            "/|\\",
            "/ \\",
        },
    }

    return hangman[stage]